#Arithmetic Operators (+,-,*,/,//,**,%)
a = 75
b = 55 
c = a+b
print(c)

#Assignment Operator (=,+=,-=,*=,%=,**=,/=,//=)
a = 75-3 # Assign 75-3 in a
print(a)
b=88
b +=44  # Increment the value of b by 44 and then assign it to b
b -=66 # Decrement the value of b by 66 and then assign it to b
print(b)

#Comparison Operator (<,>,<=,>=,==,!=)

d = 5==5 
print(d)

#Logical Operator(or,and,not)
e = True or False
# Truth table of 'or' 
print("True or False is ", True or False)
print("True or True is ", True or True)
print("False or True is ", False or True)
print("False or False is ", False or False)

# Truth table of 'and' 
print("True and False is ", True and False)
print("True and True is ", True and True)
print("False and True is ", False and True)
print("False and False is ", False and False)

print(not(True))

#Bitwise Operators (&,|,~,^,>>,<<)
f = 10
g = 4
print(f<<g)
print(f>>g)
print(f|g)
print(f&g)

